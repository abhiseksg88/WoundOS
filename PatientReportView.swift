import SwiftUI

struct PatientReportView: View {
    @StateObject private var store = ScanStore.shared
    
    var body: some View {
        NavigationStack {
            Group {
                if store.records.isEmpty {
                    emptyState
                } else if let latest = store.records.first,
                          let measurement = store.loadMeasurement(for: latest),
                          let primary = measurement.primary {
                    reportContent(record: latest, measurement: measurement, region: primary)
                } else {
                    emptyState
                }
            }
            .navigationTitle("Report")
        }
    }
    
    private var emptyState: some View {
        VStack(spacing: 16) {
            Image(systemName: "doc.text")
                .font(.system(size: 48))
                .foregroundColor(.secondary)
            Text("No Reports Available")
                .font(.title3.weight(.medium))
            Text("Complete a wound scan to generate a clinical report.")
                .font(.subheadline)
                .foregroundColor(.secondary)
                .multilineTextAlignment(.center)
        }
        .padding(40)
    }
    
    private func reportContent(record: ScanRecord, measurement: WoundMeasurement, region: RegionMeasurement) -> some View {
        ScrollView {
            VStack(alignment: .leading, spacing: 16) {
                // Header
                VStack(alignment: .leading, spacing: 4) {
                    Text("CarePlix WoundOS")
                        .font(.title2.weight(.bold))
                    Text("Wound Measurement Report")
                        .font(.subheadline)
                        .foregroundColor(.secondary)
                    
                    Divider()
                    
                    Text("Date: \(record.timestamp.formatted(date: .long, time: .shortened))")
                        .font(.caption)
                        .foregroundColor(.secondary)
                }
                
                // Wound Image
                if let image = store.loadOverlayImage(for: record) ?? store.loadCaptureImage(for: record) {
                    Image(uiImage: image)
                        .resizable()
                        .aspectRatio(contentMode: .fit)
                        .cornerRadius(12)
                }
                
                // Key Measurements
                SectionCard {
                    VStack(alignment: .leading, spacing: 12) {
                        Text("Key Measurements")
                            .font(.headline)
                        
                        LazyVGrid(columns: [GridItem(.flexible()), GridItem(.flexible())], spacing: 12) {
                            measurementItem("Area", "\(String(format: "%.2f", region.areaCm2)) cm²")
                            measurementItem("Length", "\(String(format: "%.1f", region.maxLengthCm)) cm")
                            measurementItem("Width", "\(String(format: "%.1f", region.maxWidthCm)) cm")
                            measurementItem("Depth", "\(String(format: "%.1f", region.maxDepthMm)) mm")
                            measurementItem("Volume", "\(String(format: "%.1f", region.volumeMl)) mL")
                            measurementItem("Perimeter", "\(String(format: "%.1f", region.perimeterCm)) cm")
                        }
                    }
                }
                
                // Clinical Summary
                SectionCard {
                    VStack(alignment: .leading, spacing: 8) {
                        Text("Clinical Summary")
                            .font(.headline)
                        
                        Text(ClinicalSummaryGenerator.generate(measurement: measurement, region: region))
                            .font(.subheadline)
                            .foregroundColor(.secondary)
                            .lineSpacing(4)
                    }
                }
                
                // Disclaimer
                Text("Generated by CarePlix WoundOS v1.0.0. This report is for clinical reference only. Not a substitute for clinical assessment.")
                    .font(.caption2)
                    .foregroundColor(.secondary)
                    .multilineTextAlignment(.center)
                    .padding(.top, 8)
            }
            .padding()
        }
        .background(Theme.Colors.groupedBackground)
    }
    
    private func measurementItem(_ label: String, _ value: String) -> some View {
        VStack(alignment: .leading, spacing: 2) {
            Text(label)
                .font(.caption)
                .foregroundColor(.secondary)
            Text(value)
                .font(.subheadline.weight(.semibold))
        }
        .frame(maxWidth: .infinity, alignment: .leading)
        .padding(10)
        .background(Theme.Colors.groupedBackground)
        .cornerRadius(8)
    }
}
